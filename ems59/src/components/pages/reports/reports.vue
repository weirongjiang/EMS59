<template>
    <div id="echart">

    </div>
</template>

<script>
export default {
    mounted(){
        //基于准备好的dom，初始化echarts实列
        var myChart = this.$echarts.init(document.getElementById('echart'));
        //指定图表的配置和数据
        var option = {
            title:{
                text:'ECharts 入门示列'
            },
            tooltip:{},
            legend:{
                data:['销量']
            },
            xAxis:{
                data:["衬衫", "羊毛衫", "雪纺衫", "裤子", "高跟鞋", "袜子"]

            },
            yAxis: {},
            series: [{
                name: '销量',
                type: 'bar',
                data: [5, 20, 36, 10, 10, 20]
            }]
        };

          // 使用刚指定的配置项和数据显示图表。
          myChart.setOption(option);
    }
}
</script>

<style>
#echart {
    width: 100%;
    height: 100%;
}
